"""
AI 裂变助手 (AI Fission Assistant)
================================================================================
【核心系统提示词配置中心】

================================================================================
【项目愿景与核心逻辑说明 (For AI Agents/LLMs)】
本项目旨在为电商卖家提供“一张产品或模特原图 -> 自动化裂变出 N 种高质感场景与造型变体”的图生图 (Image-to-Image) 体验。
项目采用了【双 AI 模型协同】的架构设计：

▶ 角色 1：语言大模型 LLM（如 DeepSeek, GPT-4、Claude 等）—— 担任“创意先锋/提示词架构师”。
   - 【输入】：用户的全局概念提示词（例如：“西方室内场景”）、生成张数、以及特殊的“人物锁”、“场景锁”开关状态。
   - 【职责】：根据单薄的“全局提示词”，在脑海中发散并扩写出 N 条细节丰富、具有差异化（不同光线、陈设、角度）的具体渲染指令。
   - 【输出】：一个标准的 JSON 数组 `{"prompts": ["创意描述1", "创意描述2"...]}`。

▶ 角色 2：多模态图像生成大模型（如 Gemini Imagen、Midjourney 等）—— 担任“终极画师/修版师”。
   - 【输入】：由上一阶段打散出来的“单条创意提示词”，加上【系统级终极约束】（即绝对不得改变产品特征指令），并附带了交替的多张参考图片。
     这可能包括：
      [图A: 目标底图] - 提供人物/产品的原始姿态动作。
      [图B: 人物锁定图] (按需) - 给画师看脸、看形象，强调照抄这图的脸，但不能抄这图的姿势。
      [图C: 场景参考图] (按需) - 给画师看风格、环境、色调，并把最终合成的主体融入这个环境。
   - 【职责】：严格遵循约束底线，根据传回的组合指令完成最后的渲染。

在优化的开发过程中，本文件集中存储了以上所有环节的 Prompt 原始模版。
只要阅读且修改此文件，便可对大模型的输出方向产生最底层的根本影响。
================================================================================
"""

